//Tanımlamalar

//Expanded:Row, Column veya Flex içinde bulunan bir widget’ın kalan tüm boş alanı doldurmasını sağlar.
//Flexible:Row, Column veya Flex içinde bulunan bir widget’ın esnek bir şekilde boyutlandırılmasını sağlar.
//Stack:Widget’ları üst üste yerleştirmek için kullanılır.
//Positioned:Stack içinde bulunan widget’ların konumlandırılmasını sağlar.
//Align:Bir widget’ı belirli bir konuma hizalamak için kullanılır.
//Padding:Bir widget’ın etrafına boşluk eklemek için kullanılır.
//Center:Bir widget’ı hem yatay hem de dikey olarak ortalamak için kullanılır.
//SizedBox:Belirli bir boyutta boşluk oluşturmak veya bir widget
//için belirli bir boyut ayarlamak için kullanılır.
//AspectRatio:Bir widget  için belirli bir en -boy oranı ayarlamak için kullanılır.
//FractionallySizedBox:Bir widget’ın boyutunu ebeveyninin boyutuna göre ayarlamak için kullanılır.
//OverflowBox:Bir widget’ın boyutunu ebeveyninin boyutundan daha büyük yapmasına izin verir.
//LimitedBox:Bir widget’ın maksimum boyutlarını sınırlamak için kullanılır.
//IntrinsicWidth ve IntrinsicHeight:Bir widget’ın doğal boyutlarını kullanarak boyutlandırılmasını sağlar.
//MediaQuery:Ekran boyutları ve yönlendirme gibi bilgileri almak için kullanılır.
//LayoutBuilder:Ebeveynin boyutlarına göre widget’ları dinamik olarak oluşturmak için kullanılır.
